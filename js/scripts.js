function newMechanic() { return mechanics[Math.floor(Math.random() * count)]; }

function getMechanics() {
    const localMechanics = [];
    while (localMechanics.length < 6)
    {
        let mechanic = newMechanic();
        if (!localMechanics.includes(mechanic)) localMechanics.push(mechanic);
    }
    
    document.getElementById("list").innerHTML = "<li>" + localMechanics.join("</li><li>") + "</li>";
} 